<?php

namespace Jebpkgs\ContactUs\Http;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use Jebpkgs\ContactUs\Http\Requests\StoreContactUs;
use Jebpkgs\ContactUs\Model\ContactUs;

class ContactUsController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $list = ContactUs::all();
        return view('contactus::list', compact('list'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('contactus::form');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreContactUs $request)
    {
        $input = $request->all();
        $contactus = new ContactUs;
        $contactus->create($input);
        return redirect()->route('contactus.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $contactus = ContactUs::findOrFail($id);
        return view('contactus::form', compact('contactus'));

    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $input = $request->all();
        $contactus = ContactUs::findOrFail($id);
        $contactus->update($input);
        return redirect()->route('contactus.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
    }
}
